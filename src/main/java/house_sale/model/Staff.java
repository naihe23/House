package house_sale.model;

public class Staff {
	   private long staff_ID;
	   private String staff_name;
	   private String staff_password;
	   private String staff_sex;
	   private String staff_real_name;
	   private int staff_age;
	   private int staff_state;
	public long getStaff_ID() {
		return staff_ID;
	}
	public void setStaff_ID(long staff_ID) {
		this.staff_ID = staff_ID;
	}
	public String getStaff_name() {
		return staff_name;
	}
	public void setStaff_name(String staff_name) {
		this.staff_name = staff_name;
	}
	public String getStaff_password() {
		return staff_password;
	}
	public void setStaff_password(String staff_password) {
		this.staff_password = staff_password;
	}
	public String getStaff_sex() {
		return staff_sex;
	}
	public void setStaff_sex(String staff_sex) {
		this.staff_sex = staff_sex;
	}
	public String getStaff_real_name() {
		return staff_real_name;
	}
	public void setStaff_real_name(String staff_real_name) {
		this.staff_real_name = staff_real_name;
	}
	public int getStaff_age() {
		return staff_age;
	}
	public void setStaff_age(int staff_age) {
		this.staff_age = staff_age;
	}
	public int getStaff_state() {
		return staff_state;
	}
	public void setStaff_state(int staff_state) {
		this.staff_state = staff_state;
	}
	   
	   
}
